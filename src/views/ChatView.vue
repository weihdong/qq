  }
  
// WebSocket ËøûÊé•ÁÆ°ÁêÜÔºà‰ºòÂåñÈáçËøûÈÄªËæëÔºâ
const connectWebSocket = () => {
  const ws = new WebSocket(getWsURL())
  let heartbeatInterval

  const sendConnect = () => {
    if (ws.readyState === WebSocket.OPEN) {
      ws.send(JSON.stringify({
        type: 'connect',
        userId: localStorage.getItem('userId')
      }))
    }
  }

  ws.onopen = () => {
    console.log('WebSocketËøûÊé•ÊàêÂäü')
    reconnectAttempts = 0
    sendConnect()
    
    // ÂøÉË∑≥Êú∫Âà∂ÔºàÊØè25ÁßíÂèëÈÄÅ‰∏ÄÊ¨°Ôºâ
    heartbeatInterval = setInterval(() => {
      if (ws.readyState === WebSocket.OPEN) {
        ws.send(JSON.stringify({ type: 'ping' }))
      }
    }, 25000)
  }
  
  ws.onmessage = (event) => {
    try {
      const message = JSON.parse(event.data)
      switch (message.type) {
        case 'text':   // Â§ÑÁêÜÊñáÊú¨Ê∂àÊÅØ
        case 'image':  // Â§ÑÁêÜÂõæÁâáÊ∂àÊÅØ
        case 'audio':  // Â§ÑÁêÜËØ≠Èü≥Ê∂àÊÅØ
          store.messages.push(message)
          break
        case 'status':
          const friend = store.friends.find(f => f._id === message.userId)
          if (friend) friend.isOnline = message.online
          break
        case 'system':
          console.log('Á≥ªÁªüÊ∂àÊÅØ:', message.message)
          break
      }
    } catch (error) {
      console.error('Ê∂àÊÅØËß£ÊûêÈîôËØØ:', error)
    }
  }
  
  // Êñ∞Â¢ûÔºöÂú®Êî∂Âà∞ÂÖ≥Èó≠‰∫ã‰ª∂Êó∂Â∞ùËØïÁ´ãÂç≥ÈáçËøû
  ws.onclose = (event) => {
    console.log('ËøûÊé•ÂÖ≥Èó≠Ôºå‰ª£Á†Å:', event.code, 'ÂéüÂõ†:', event.reason)
    clearInterval(heartbeatInterval)
    
    // Á´ãÂç≥Â∞ùËØïÈáçËøûÔºàÊåáÊï∞ÈÄÄÈÅøÔºâ
    const reconnect = () => {
      if (reconnectAttempts < MAX_RECONNECT_ATTEMPTS) {
        console.log(`Â∞ùËØïÁ¨¨ ${reconnectAttempts + 1} Ê¨°ÈáçËøû...`)
        store.ws = connectWebSocket()
        reconnectAttempts++
      }
    }
    
    // È¶ñÊ¨°Á´ãÂç≥ÈáçËøûÔºåÂêéÁª≠‰ΩøÁî®Âª∂Ëøü
    if (reconnectAttempts === 0) {
      reconnect()
    } else {
      const delay = Math.min(3000 * Math.pow(2, reconnectAttempts), 30000)
      setTimeout(reconnect, delay)
    }
  }
  
    ws.onerror = (error) => {
      console.error('WebSocketÈîôËØØ:', error)
    }
  
    return ws
  }
  
  // ÈÄÄÂá∫ÁôªÂΩï
  const logout = () => {
    localStorage.removeItem('userId')
    localStorage.removeItem('username')
    router.push('/login')
  }
  
  // Ê∑ªÂä†Â•ΩÂèãÂäüËÉΩ
  const toggleAddFriend = () => {
    showAddFriendModal.value = !showAddFriendModal.value
    newFriendName.value = ''
  }
  
  const currentFriend = computed(() => {
    return store.friends.find(f => f._id === store.currentChat)
  })
  
  const currentPlaceholder = computed(() => {
    if (!store.currentChat) return 'ÁÇπÂáªÈ°∂Ê†èÂ§¥ÂÉèËøõË°åËÅäÂ§©ÂêßÔºÅ'
    return currentFriend.value
      ? `Áªô ${currentFriend.value.username} ÂèëÈÄÅÊ∂àÊÅØ`
      : 'Ê≠£Âú®Âä†ËΩΩÁî®Êà∑‰ø°ÊÅØ...'
  })
  
// ‰ºòÂåñÂêéÁöÑÊ∑ªÂä†Â•ΩÂèãÊñπÊ≥ï
const addFriend = async () => {
  try {
    if (!newFriendName.value.trim()) {
      alert('ËØ∑ËæìÂÖ•Â•ΩÂèãÁî®Êà∑Âêç');
      return;
    }

    const response = await axios.post(`${getBaseURL()}/api/friends`, {
      userId: localStorage.getItem('userId'),
      friendUsername: newFriendName.value.trim()
    });

    if (response.data?.friendId) {
      // Ë∞ÉÁî®Êñ∞Ê∑ªÂä†ÁöÑËé∑ÂèñÁî®Êà∑‰ø°ÊÅØÊé•Âè£
      const friendInfo = await axios.get(`${getBaseURL()}/api/user/${response.data.friendId}`)
      
      store.friends.push({
        _id: response.data.friendId,
        username: friendInfo.data.username,
        isOnline: false // Ê≠§Â§ÑÂèØ‰ª•ÁªìÂêàWebSocketÁä∂ÊÄÅÊõ¥Êñ∞
      });
      
      toggleAddFriend();
      alert('Ê∑ªÂä†ÊàêÂäüÔºÅ');
    }
  } catch (error) {
    let errorMessage = 'Ê∑ªÂä†Â§±Ë¥•ÔºåËØ∑ÈáçËØï';
    if (error.response) {
      switch (error.response.data.code) {
        case 'FRIEND_NOT_FOUND':
          errorMessage = 'Áî®Êà∑‰∏çÂ≠òÂú®';
          break;
        case 'ALREADY_FRIENDS':
          errorMessage = 'Â∑≤ÊòØÂ•ΩÂèãÂÖ≥Á≥ª';
          break;
        case 'SELF_ADDITION':
          errorMessage = '‰∏çËÉΩÊ∑ªÂä†Ëá™Â∑±';
          break;
      }
    }
    alert(`ÈîôËØØ: ${errorMessage}`);
    console.error('Ê∑ªÂä†Â•ΩÂèãÂ§±Ë¥•ËØ¶ÊÉÖ:', error.response?.data || error.message);
  }
}
  
  // ÈÄâÊã©Â•ΩÂèã
  const selectFriend = async (friendId) => {
    store.currentChat = friendId
    await store.loadMessages()
  }
  
// Êñ∞Â¢ûÂìçÂ∫îÂºèÂèòÈáè
const isRecording = ref(false)
let mediaRecorder = null
let audioChunks = ref([])

// ÂõæÁâá‰∏ä‰º†Â§ÑÁêÜ
const handleImageUpload = async (event) => {
  const file = event.target.files[0]
  if (!file) return
  
  try {
    // Ê£ÄÊü•Êñá‰ª∂Á±ªÂûã
    if (!file.type.match('image.*')) {
      alert('ËØ∑ÈÄâÊã©ÂõæÁâáÊñá‰ª∂')
      return
    }
    
    // ÈôêÂà∂ÂõæÁâáÂ§ßÂ∞è (‰æãÂ¶Ç2MB)
    if (file.size > 2 * 1024 * 1024) {
      alert('ÂõæÁâáÂ§ßÂ∞è‰∏çËÉΩË∂ÖËøá2MB')
      return
    }
    
    const reader = new FileReader()
    reader.onload = async (e) => {
      // Ëé∑ÂèñBase64ÁºñÁ†ÅÁöÑÂõæÁâá
      const base64Image = e.target.result
      
      // ÂèëÈÄÅÂõæÁâáÊ∂àÊÅØ
      if (store.ws && store.ws.readyState === WebSocket.OPEN) {
        const message = {
          type: 'image',
          from: userId,
          to: store.currentChat,
          content: base64Image,
          timestamp: new Date().toISOString()
        }
        
        store.ws.send(JSON.stringify(message))
        store.messages.push(message)
      } else {
        console.error('WebSocketËøûÊé•Êú™Â∞±Áª™')
        alert('ÂèëÈÄÅÂ§±Ë¥•ÔºåËØ∑Ê£ÄÊü•ÁΩëÁªúËøûÊé•')
      }
    }
    
    reader.readAsDataURL(file)
    // ÈáçÁΩÆinputÔºåÂÖÅËÆ∏ÂÜçÊ¨°ÈÄâÊã©Âêå‰∏ÄÊñá‰ª∂
    event.target.value = ''
  } catch (error) {
    console.error('ÂõæÁâá‰∏ä‰º†Â§±Ë¥•:', error)
    alert('ÂõæÁâá‰∏ä‰º†Â§±Ë¥•')
  }
}

// ËØ≠Èü≥ÂΩïÂà∂ÂäüËÉΩ
const toggleVoiceRecord = async () => {
  if (isRecording.value) {
    // ÂÅúÊ≠¢ÂΩïÈü≥
    if (mediaRecorder && mediaRecorder.state !== 'inactive') {
      mediaRecorder.stop()
    }
    isRecording.value = false
    return
  }
  
  try {
    // ËØ∑Ê±ÇÈ∫¶ÂÖãÈ£éÊùÉÈôê
    const stream = await navigator.mediaDevices.getUserMedia({ audio: true })
    mediaRecorder = new MediaRecorder(stream)
    audioChunks.value = []
    
    mediaRecorder.ondataavailable = (event) => {
      if (event.data.size > 0) {
        audioChunks.value.push(event.data)
      }
    }
    
    mediaRecorder.onstop = async () => {
      // ÂêàÂπ∂Èü≥È¢ëÁâáÊÆµ
      const audioBlob = new Blob(audioChunks.value, { type: 'audio/webm' })
      
      // ËΩ¨Êç¢‰∏∫Base64
      const reader = new FileReader()
      reader.onload = () => {
        const base64Audio = reader.result
        
        // ÂèëÈÄÅËØ≠Èü≥Ê∂àÊÅØ
        if (store.ws && store.ws.readyState === WebSocket.OPEN) {
          const message = {
            type: 'audio',
            from: userId,
            to: store.currentChat,
            content: base64Audio,
            timestamp: new Date().toISOString()
          }
          
          store.ws.send(JSON.stringify(message))
          store.messages.push(message)
        }
      }
      
      reader.readAsDataURL(audioBlob)
      
      // ÂÖ≥Èó≠Â™í‰ΩìÊµÅ
      stream.getTracks().forEach(track => track.stop())
    }
    
    // ÂºÄÂßãÂΩïÈü≥
    mediaRecorder.start()
    isRecording.value = true
    
    // ËÆæÁΩÆ60ÁßíËá™Âä®ÂÅúÊ≠¢
    setTimeout(() => {
      if (isRecording.value) {
        toggleVoiceRecord()
      }
    }, 60000)
  } catch (error) {
    console.error('Êó†Ê≥ïËÆøÈóÆÈ∫¶ÂÖãÈ£é:', error)
    alert('Êó†Ê≥ïËÆøÈóÆÈ∫¶ÂÖãÈ£éÔºåËØ∑Ê£ÄÊü•ÊùÉÈôêËÆæÁΩÆ')
  }
}

  // ÂèëÈÄÅÊ∂àÊÅØÔºàÂ¢ûÂº∫ÁâàÔºâ
  const sendMessage = () => {
    if (!newMessage.value.trim()) return
  
    if (!store.ws || store.ws.readyState !== WebSocket.OPEN) {
      console.log('ËøûÊé•Êú™Â∞±Áª™ÔºåÂ∞ùËØïÈáçÊñ∞ÂèëÈÄÅ...')
      store.ws = connectWebSocket()
      setTimeout(sendMessage, 500)
      return
    }
  
    try {
      const message = {
        type: 'text',
        from: userId,
        to: store.currentChat,
        content: newMessage.value.trim(),
        timestamp: new Date().toISOString()
      }
      
      store.ws.send(JSON.stringify(message))
      newMessage.value = ''
    } catch (error) {
      console.error('ÂèëÈÄÅÊ∂àÊÅØÂ§±Ë¥•:', error)
      alert('Ê∂àÊÅØÂèëÈÄÅÂ§±Ë¥•ÔºåËØ∑Ê£ÄÊü•ÁΩëÁªúËøûÊé•')
    }
  }
  
  // Êó∂Èó¥Ê†ºÂºèÂåñ
  const formatTime = (timestamp) => {
    return new Date(timestamp).toLocaleString('zh-CN', {
      year: 'numeric',
      month: '2-digit',
      day: '2-digit',
      hour: '2-digit',
      minute: '2-digit',
      second: '2-digit'
    })
  }
  
  // Ê∂àÊÅØËá™Âä®ÊªöÂä®
  watch(() => store.messages, async () => {
    await nextTick()
    if (chatArea.value) {
      chatArea.value.scrollTop = chatArea.value.scrollHeight
    }
  }, { deep: true })
  
  // ÂàùÂßãÂåñÂä†ËΩΩ
  onMounted(async () => {
    try {
      // Âä†ËΩΩÂ•ΩÂèãÂàóË°®
      const friendsRes = await axios.get(`${getBaseURL()}/api/friends`, {
        params: { userId }
      })
      store.friends = friendsRes.data.friends.map(f => ({
        ...f,
        isOnline: false // ÂàùÂßãÁä∂ÊÄÅËÆæ‰∏∫Á¶ªÁ∫ø
      }))
  
      // Âª∫Á´ãWebSocketËøûÊé•
      store.ws = connectWebSocket()
    } catch (error) {
      console.error('ÂàùÂßãÂåñÂ§±Ë¥•:', error)
      alert('ÂàùÂßãÂåñÂ§±Ë¥•ÔºåËØ∑Âà∑Êñ∞È°µÈù¢ÈáçËØï')
    }
  })
  </script>
  
